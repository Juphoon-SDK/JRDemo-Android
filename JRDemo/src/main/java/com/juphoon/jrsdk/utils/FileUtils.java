    public static String getMimeType(String fileName) {
        if (TextUtils.isEmpty(fileName)) {
            return null;
        }
        String type = "*/*";
        int dotIndex = fileName.lastIndexOf(".");
        if (dotIndex < 0) {
            return type;
        }
        String end = fileName.substring(dotIndex, fileName.length()).toLowerCase(Locale.getDefault());
        if (TextUtils.isEmpty(end)) {
            return type;
        }
        for (int i = 0; i < MIME_MAP_TABLE.length; i++) {
            if (end.equals(MIME_MAP_TABLE[i][0])) {
                type = MIME_MAP_TABLE[i][1];
                break;
            }
        }
        return type;
    }

    public static int computeSampleSize(int width, int height, int minSideLength, int maxNumOfPixels) {
        int initialSize = computeInitialSampleSize(width, height, minSideLength, maxNumOfPixels);
        int roundedSize;
        if (initialSize <= 8) {
            roundedSize = 1;
            while (roundedSize < initialSize) {
                roundedSize <<= 1;
            }
        } else {
            roundedSize = (initialSize + 7) / 8 * 8;
        }
        return roundedSize;
    }

    private static int computeInitialSampleSize(int width, int height, int minSideLength, int maxNumOfPixels) {
        double w = width;
        double h = height;
        int lowerBound = (maxNumOfPixels == -1) ? 1 : (int) Math.ceil(Math.sqrt(w * h / maxNumOfPixels));
        int upperBound = (minSideLength == -1) ? 128 : (int) Math.min(Math.floor(w / minSideLength),
                Math.floor(h / minSideLength));
        if (upperBound < lowerBound) {
            // return the larger one when there is no overlapping zone.
            return lowerBound;
        }
        if ((maxNumOfPixels == -1) && (minSideLength == -1)) {
            return 1;
        } else if (minSideLength == -1) {
            return lowerBound;
        } else {
            return upperBound;
        }
    }

    /**
     * 获取音视频长度
     */
    public static int getMediaDuring(String path) {
        if (!new File(path).exists())
            return 0;

        MediaPlayer player = new MediaPlayer();
        int seconds = 0;
        try {
            player.setDataSource(path);
            player.prepare();
            seconds = (player.getDuration() + 500) / 1000;
            if (seconds == 0) {
                seconds = 1;
            }
            player.release();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return seconds;
    }
